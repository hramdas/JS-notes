var size = "XL";
switch (size) {
    case "S":
        console.log("Small");
        break;
    case "M":
        console.log("Medium");
        break;
    case "L":
        console.log("Large");
        break;
    case "XL":
        console.log("Extra Large");
        break;
    default:
        console.log("None");
        break;
}